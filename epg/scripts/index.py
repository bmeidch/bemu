#!/usr/bin/env python3

# EPG Merger (Optimized v3.0 - Low Memory & High Speed)
# Original: @thefirefox12537

import glob
import os
import re
import sys
import shutil
import traceback
import requests
import lxml.etree as et

from concurrent.futures import ThreadPoolExecutor, as_completed

# ----------------------------------------------------
# Arguments & Initialization
# ----------------------------------------------------

if len(sys.argv) < 5:
    print("[ERROR] Missing arguments.")
    print("Usage: python epg_merger.py <RESULT> <SOURCE> <CAPTION_NAME> <CAPTION_URL>")
    sys.exit(1)

_, result, source, caption_name, caption_url = sys.argv

tmpdir = os.path.join("..", "tmp")

# HTTP Session (Connection Pool)
session = requests.Session()
session.headers.update({
    "User-Agent": "EPG Merger/3.0"
})

# ----------------------------------------------------
# Helpers
# ----------------------------------------------------

def load_mappings():
    """Membaca semua file mapping (.txt) sekali saja di awal untuk menghemat I/O"""
    mappings = {}
    files = glob.glob(os.path.join(tmpdir, "*.xml"))
    for file in files:
        base_name = os.path.basename(file)
        epgid_path = os.path.join(os.path.dirname(source), base_name + ".txt")
        
        mappings[base_name] = {}
        if os.path.exists(epgid_path):
            with open(epgid_path, encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line or "," not in line:
                        continue
                    old, new = line.split(",", 1)
                    mappings[base_name][old] = new
    return mappings

# ----------------------------------------------------
# Download Function
# ----------------------------------------------------

def download(url, filename):
    epgxml = os.path.join(tmpdir, filename)

    if os.path.exists(epgxml):
        print(f"[SKIP] {filename}")
        return

    try:
        print(f"[DOWN] {filename}")
        response = session.get(
            url,
            timeout=30,
            allow_redirects=True
        )
        response.raise_for_status()

        with open(epgxml, "wb") as f:
            f.write(response.content)

        print(f"[ OK ] {filename}")
    except Exception:
        print(f"[FAIL] {filename}")
        traceback.print_exc()
        raise

# ----------------------------------------------------
# Streaming Merge Engine (Low Memory)
# ----------------------------------------------------

def stream_merge(output_file, mappings):
    print("\n=== Merging XML Data (Streaming Mode) ===")
    
    files = sorted(glob.glob(os.path.join(tmpdir, "*.xml")))
    
    # Buka file output dalam mode streaming writer
    with et.xmlfile(output_file, encoding="UTF-8") as xf:
        xf.write_declaration()
        
        # Bungkus dengan root elemen <tv> beserta atributnya
        root_attrib = {
            "generator-info-name": f"EPG generated by {caption_name}",
            "generator-info-url": caption_url
        }
        
        with xf.element("tv", root_attrib):
            
            # PHASE 1: Tulis seluruh elemen <channel> terlebih dahulu
            print("Processing channels...")
            for file in files:
                base_name = os.path.basename(file)
                mapping = mappings.get(base_name, {})
                
                # Iterparse memproses XML baris demi baris tanpa menumpuk di RAM
                context = et.iterparse(file, events=("end",), tag="channel")
                for _, elem in context:
                    cid = elem.attrib.get("id")
                    if cid in mapping:
                        elem.attrib["id"] = mapping[cid]
                    
                    xf.write(elem)
                    elem.clear() # Hapus elemen dari RAM setelah ditulis
                del context

            # PHASE 2: Tulis seluruh elemen <programme> setelah channel selesai
            print("Processing programmes...")
            for file in files:
                base_name = os.path.basename(file)
                mapping = mappings.get(base_name, {})
                
                context = et.iterparse(file, events=("end",), tag="programme")
                for _, elem in context:
                    channel = elem.attrib.get("channel")
                    if channel in mapping:
                        elem.attrib["channel"] = mapping[channel]
                    
                    xf.write(elem)
                    elem.clear() # Hapus elemen dari RAM setelah ditulis
                del context

# ----------------------------------------------------
# Main Worklow
# ----------------------------------------------------

def main():
    files = []
    urls = []

    if os.path.exists(result):
        os.remove(result)

    os.makedirs(tmpdir, exist_ok=True)

    if not os.path.exists(source):
        print(f"[ERROR] Source file not found: {source}")
        sys.exit(1)

    with open(source, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            if line.startswith("http"):
                urls.append(line)
            else:
                files.append(line)

    if len(files) != len(urls):
        print("[ERROR] Number of filenames and URLs do not match.")
        print(f"Files : {len(files)} | URLs : {len(urls)}")
        sys.exit(1)

    print(f"\nDownloading {len(files)} XML files...\n")

    with ThreadPoolExecutor(max_workers=8) as executor:
        futures = [
            executor.submit(download, url, name)
            for url, name in zip(urls, files)
        ]
        for future in as_completed(futures):
            future.result()

    # Load mapping dictionary ke memory sekali jalan
    mappings = load_mappings()

    # Eksekusi streaming merger
    stream_merge(result, mappings)

    print("\nCleaning temporary directory...")
    shutil.rmtree(tmpdir, ignore_errors=True)

    print("\nFinished successfully.")

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\nCancelled by user.")
        sys.exit(130)
    except Exception:
        print("\nUnexpected error:\n")
        traceback.print_exc()
        sys.exit(1)
    finally:
        session.close()
