#!/usr/bin/env python3

import glob
import os
import re
import shutil
import sys
from concurrent.futures import ThreadPoolExecutor

import lxml.etree as et
import requests

_, result, source, caption_name, caption_url = sys.argv

tmpdir = os.path.join("..", "tmp")

session = requests.Session()

mapping_cache = {}


def load_mapping(path):
    if path in mapping_cache:
        return mapping_cache[path]

    mapping = {}

    if os.path.exists(path):
        with open(path, encoding="utf-8") as f:
            for line in f:
                line = line.strip()

                if not line or "," not in line:
                    continue

                old, new = line.split(",", 1)
                mapping[old] = new

    mapping_cache[path] = mapping
    return mapping


def download(item):
    url, name = item

    outfile = os.path.join(tmpdir, name)

    if os.path.exists(outfile):
        return

    print(f"Downloading {name}...")

    try:
        r = session.get(url, timeout=60)
        r.raise_for_status()

        with open(outfile, "wb") as f:
            f.write(r.content)

    except Exception as e:
        print("Download failed:", name, e)


def merge(tree):
    files = glob.glob(os.path.join(tmpdir, "*.xml"))

    for file in files:

        try:

            root = et.parse(file).getroot()

            mapping = load_mapping(
                os.path.join(
                    os.path.dirname(source),
                    os.path.basename(file) + ".txt"
                )
            )

            for child in root:

                if child.tag == "channel":

                    cid = child.attrib.get("id")

                    if cid in mapping:
                        child.attrib["id"] = mapping[cid]

                    tree.append(child)

                elif child.tag == "programme":

                    cid = child.attrib.get("channel")

                    if cid in mapping:
                        child.attrib["channel"] = mapping[cid]

                    tree.append(child)

        except Exception as e:
            print("Skipping", file, e)


def main():

    if os.path.exists(result):
        os.remove(result)

    os.makedirs(tmpdir, exist_ok=True)

    if not os.path.exists(source):
        print(source, "not found")
        sys.exit(1)

    urls = []
    files = []

    with open(source, encoding="utf-8") as f:

        for line in f:

            line = line.strip()

            if not line:
                continue

            if line.startswith("http"):
                urls.append(line)
            else:
                files.append(line)

    print("Downloading EPG sources...")

    with ThreadPoolExecutor(max_workers=8) as executor:
        executor.map(download, zip(urls, files))

    print("Merging XML...")

    tree = et.Element(
        "tv",
        attrib={
            "generator-info-name": f"EPG generated by {caption_name}",
            "generator-info-url": caption_url,
        },
    )

    merge(tree)

    print("Writing output...")

    et.ElementTree(tree).write(
        result,
        encoding="UTF-8",
        xml_declaration=True,
        pretty_print=False,
    )

    print("Cleaning...")

    shutil.rmtree(tmpdir, ignore_errors=True)

    print("Done.")


if __name__ == "__main__":
    main()
