#!/usr/bin/env python3

# EPG Merger (Optimized)
# Original: @thefirefox12537
# Optimized: Parallel Download, Better Error Handling

import glob
import os
import re
import sys
import shutil
import traceback
import requests
import xml.dom.minidom as dom
import lxml.etree as et

from concurrent.futures import ThreadPoolExecutor, as_completed

# ----------------------------------------------------
# Arguments
# ----------------------------------------------------

exec, result, source, caption_name, caption_url = sys.argv

tmpdir = os.path.join("..", "tmp")

# HTTP Session (Connection Pool)
session = requests.Session()
session.headers.update({
    "User-Agent": "EPG Merger/2.0"
})

# ----------------------------------------------------
# Download Function
# ----------------------------------------------------

def download(url, filename):

    epgxml = os.path.join(tmpdir, filename)

    if os.path.exists(epgxml):
        print(f"[SKIP] {filename}")
        return

    try:

        print(f"[DOWN] {filename}")

        response = session.get(
            url,
            timeout=30,
            allow_redirects=True
        )

        response.raise_for_status()

        with open(epgxml, "wb") as f:
            f.write(response.content)

        print(f"[ OK ] {filename}")

    except Exception:

        print(f"[FAIL] {filename}")

        traceback.print_exc()

        raise

# ----------------------------------------------------
# Merge XML
# ----------------------------------------------------

def merge(tree, tagname, attrib):

    print(f"\n=== Merging {tagname} ===")

    files = glob.glob(os.path.join(tmpdir, "*.xml"))

    for file in sorted(files):

        try:

            print(f"[READ] {os.path.basename(file)}")

            root = et.parse(file).getroot()

            epgid = os.path.join(
                os.path.dirname(source),
                os.path.basename(file) + ".txt"
            )

            mapping = {}

            if os.path.exists(epgid):

                with open(epgid, encoding="utf-8") as f:

                    for line in f:

                        line = line.strip()

                        if not line or "," not in line:
                            continue

                        old, new = line.split(",", 1)

                        mapping[old] = new

            count = 0

            for child in root:

                if tagname not in child.tag:
                    continue

                key = child.attrib.get(attrib)

                if key in mapping:
                    child.attrib[attrib] = mapping[key]

                tree.append(child)

                count += 1

            print(f"[ OK ] {count} {tagname}")

        except Exception:

            print(f"[FAIL] {file}")

            traceback.print_exc()

# ----------------------------------------------------
# Main
# ----------------------------------------------------

def main():

    files = []
    urls = []

    # Remove old output
    if os.path.exists(result):
        os.remove(result)

    # Create tmp directory
    os.makedirs(tmpdir, exist_ok=True)

    # Check source list
    if not os.path.exists(source):
        print(f"[ERROR] Source file not found: {source}")
        sys.exit(1)

    # Read source list
    with open(source, encoding="utf-8") as f:

        for line in f:

            line = line.strip()

            if not line:
                continue

            if line.startswith("http"):
                urls.append(line)
            else:
                files.append(line)

    if len(files) != len(urls):

        print("[ERROR] Number of filenames and URLs do not match.")

        print(f"Files : {len(files)}")
        print(f"URLs  : {len(urls)}")

        sys.exit(1)

    print(f"\nDownloading {len(files)} XML files...\n")

    # Parallel download
    with ThreadPoolExecutor(max_workers=8) as executor:

        futures = [
            executor.submit(download, url, name)
            for url, name in zip(urls, files)
        ]

        for future in as_completed(futures):
            future.result()

    print("\nCreating XML tree...")

    comment = {
        "generator-info-name": f"EPG generated by {caption_name}",
        "generator-info-url": caption_url
    }

    tree = et.Element("tv", comment)

    merge(tree, "channel", "id")
    merge(tree, "programme", "channel")

    print("\nFormatting XML...")

    xml = et.tostring(
        tree,
        encoding="UTF-8",
        xml_declaration=True
    )

    pretty = dom.parseString(xml).toprettyxml(
        indent="",
        encoding="UTF-8"
    )

    output = re.sub(b"\n\n", b"", pretty)

    print(f"\nWriting {result}")

    with open(result, "wb") as f:
        f.write(output)

    print("\nCleaning temporary directory...")

    shutil.rmtree(tmpdir, ignore_errors=True)

    print("\nFinished successfully.")

# ----------------------------------------------------
# Entry Point
# ----------------------------------------------------

if __name__ == "__main__":

    try:

        main()

    except KeyboardInterrupt:

        print("\nCancelled by user.")
        sys.exit(130)

    except Exception:

        print("\nUnexpected error:\n")

        traceback.print_exc()

        sys.exit(1)

    finally:

        session.close()

